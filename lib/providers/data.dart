class ClimathonDataProvider with ChangeNotifier {
  List<ClimathonDataModel>? _data;

  List<ClimathonDataModel>? get data => _data;

  List<ClimathonDataModel> getLabelData(String? label) {
    if (_data == null) {
      return [];
    }
    return label == null
        ? _data!
        : _data!
            .where((value) => value.label == label)
            .cast<ClimathonDataModel>()
            .toList();
  }

  Future<void> loadData() async {
    _data = await Future.delayed(const Duration(seconds: 1))
        .then((value) => __dummy__
            .map(
              (element) => ClimathonDataModel(
                element['label'],
                element['description'],
                DateTime.parse(element['timestamp']),
                element['latitude2'],
                element['longitude2'],
                element['status'],
              ),
            )
            .toList());
    notifyListeners();
  }
}

